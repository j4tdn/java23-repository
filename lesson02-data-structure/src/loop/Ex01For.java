package loop;

public class Ex01For {
	public static void main(String[] args) {
		// for , while , do while
		//1 duyệt các phần tử từ a-b
		//-> in ra danh sách có số nguyên từ 1-10
		
		for(int i=1;i<10;i++) {
			System.out.println("i = "+i);
		}
		
		//2 duyệt các phần tử trong danh sách tập hợp
	}

}
